export function getRoomsForHotel(hotelName: string, basePrice: number, hotelId: string) {
  const rooms: any[] = [];
  const priceMultipliers = [1, 1.8, 3.5, 2.2, 5];
  
  roomTemplates.forEach((template, index) => {
    rooms.push({
      roomName: `${template.type} at ${hotelName}`,
      description: template.description,
      guests: template.guests,
      price: Math.round(basePrice * priceMultipliers[index]),
      bedType: template.bedType,
      bathrooms: template.bathrooms,
      size: template.size,
      image: `https://images.unsplash.com/photo-${1580000000000 + index * 10000000}?q=80&w=2400&auto=format&fit=crop`,
      hotelId: hotelId,
      hotelName: hotelName,
      amenities: template.amenities,
      archived: false,
      createdAt: new Date().toISOString()
    });
  });
  
  return rooms;
}
